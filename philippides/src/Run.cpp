CRun::CRun(const QDate& date, const QTime& time):
    m_Date( date ),
    m_Time( time ),
    m_nPulse( 0 ),
    m_EnWeather( NOVALUE ),
    m_EnImpression( NOVALUE )
{
    if( !m_Date.isValid() )
	throw Except::InvalidDataException( "CRun::CRun", "date" );
    if( !m_Time.isValid() )
	throw Except::InvalidDataException( "CRun::CRun", "time" );
};

CRun::~CRun()
{

};

//------------------------------------------------------------------------------
