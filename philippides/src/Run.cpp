static QString CRun::XmlHeader() const
{
    QString sXml;
    QTextStream stream( &sXml, IO_WriteOnly );

    stream  << "<?xml version=\"1.0\" encoding=\"iso-8859-1\"?>\n"
	    << DTD::szRunDtd
	    << "<rundb>\n";

return sXml;
}

static QString CRun::XmlFooter() const
{
    return QString( "</rundb>" );
}

